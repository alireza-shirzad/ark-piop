    fn track_virt_poly(&mut self, virt: VirtualPoly<F>) -> TrackerID {
        let poly_id = self.gen_id();
        self.state.virtual_polys.insert(poly_id, virt);
        // No need to commit to virtual polynomials
        poly_id
    }

